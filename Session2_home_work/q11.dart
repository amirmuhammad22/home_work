2. Describe the difference between var, dynamic, and explicitly typed variables in Dart.
*/
/*
1. var :
The var keyword is used to declare a variable whose type is inferred by the Dart compiler based on the initial value assigned to it. Once the type is inferred, it becomes fixed and cannot be changed later. This provides strong type safety while reducing verbosity.
2. dynamic :
The dynamic keyword allows a variable to hold values of any type, and its type can change at runtime. This provides maximum flexibility but sacrifices static type checking, meaning type-related errors might only appear during execution.
3. Explicitly Typed Variables:
This involves explicitly declaring the data type of a variable using keywords like String, int, double, bool, or custom class names. This offers the highest level of type safety and improves code readability by clearly indicating the expected data type.

